namespace Minefield
{
    /// <summary>
    /// Possible directions that a player can move
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// Move the player Up
        /// </summary>
        Up,

        /// <summary>
        /// Move the player Down
        /// </summary>
        Down,

        /// <summary>
        /// Move the player Left
        /// </summary>
        Left,

        /// <summary>
        /// Move the player Right
        /// </summary>
        Right,

        /// <summary>
        /// This is an invalid move
        /// </summary>
        None
    }
}